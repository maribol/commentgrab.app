const messages: Record<Locale, Record<string, string>> = { en, es, pt, fr, de };
for (const l of Object.keys(messages) as Locale[]) {
  Object.assign(messages[l], extra[l], extra2[l]);
}

const state = reactive<{ locale: Locale }>({ locale: 'en' });

export function setLocale(locale: string): void {
  state.locale = (LOCALES.some((l) => l.id === locale) ? locale : 'en') as Locale;
}

export function currentLocale(): Locale {
  return state.locale;
}

/** Translate a key for the active locale, falling back to English then the key. */
export function t(key: string, params?: Record<string, string | number>): string {
  let s = messages[state.locale]?.[key] ?? en[key] ?? key;
  if (params) for (const k of Object.keys(params)) s = s.replace(new RegExp(`\\{${k}\\}`, 'g'), String(params[k]));
  return s;
}

export function useI18n() {
  return { t, setLocale, locale: state, LOCALES };
}

/** Load the saved language and apply it. Call before mounting each entry. */
export async function initLocale(): Promise<void> {
  try {
    const s = await getSettings();
    setLocale(s.language || 'en');
  } catch {
    /* default English */
  }
}
